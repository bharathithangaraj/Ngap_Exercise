public class SwitchExample {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		String i = "10";
		switch(i) {
		case "2" : System.out.println("coming 2"); break;
		case "10" : System.out.println("coming 10"); break;
		default:System.out.println("nothing is matched");
		
		}

	}

}
